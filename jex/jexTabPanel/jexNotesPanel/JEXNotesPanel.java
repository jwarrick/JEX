package jex.jexTabPanel.jexNotesPanel;

import java.awt.BorderLayout;

import javax.swing.JPanel;

import jex.statics.DisplayStatics;
import signals.SSCenter;

public class JEXNotesPanel extends JPanel
{
	private static final long serialVersionUID = 1L;
	
	private AvailableNotesPanel     availableNotesPanel;
	private JEXNotesPanelController parent;

	JEXNotesPanel(JEXNotesPanelController parent)
	{
		this.parent = parent;
		initialize();
		
		// Connect to changes in the filtering / value to be displayed
		SSCenter.defaultCenter().connect(this.parent, JEXNotesPanelController.NAVIGATION_CHANGED, this, "changeDetected", (Class[])null);
	}
	
	/**
	 * Detach the signals
	 */
	public void deInitialize()
	{
		SSCenter.defaultCenter().disconnect(this);
	}
	
	private void initialize()
	{
		// Place the components in the split pane
		this.setLayout(new BorderLayout());
		this.setBackground(DisplayStatics.background);
		
		// Create an available notes panel
		availableNotesPanel = new AvailableNotesPanel();
		this.add(availableNotesPanel,BorderLayout.CENTER);
	}
	
	public void changeDetected()
	{
		availableNotesPanel.changeDetected();
	}
	
	
}